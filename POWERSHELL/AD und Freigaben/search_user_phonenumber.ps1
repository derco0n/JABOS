<#
Searches for a phone number in AD-Users
#>
param(
    [String]$number=""
)

if ($number -eq ""){
    Write-host "Please specifiy -number"
    exit 1
}

$allusers=Get-ADuser -filter * -properties samaccountname,mail,title,givenname,surname,telephoneNumber,department,l | select-object samaccountname,mail,title,givenname,surname,telephoneNumber,department,l

[System.Collections.ArrayList]$results= @()

$found=$false
foreach ($user in $allusers){
    if ($user.telephoneNumber -Match $number){
        $null=$results.Add($user)
        $found = $true
    }
}

if ($found){
    $results | ft
}
else {
    write-host ("Unable to find number `"" + $number + "`"")
}
