# Ermittelt alle Computerobjekte im AD
param(
[ADSI]$domain="LDAP://DC=eine,DC=firma,DC=local"
)
 
$objsearcher = New-Object DirectoryServices.DirectorySearcher($domain)	
 
#Setzt den LDAP Suchpfad auf das DirectorySearcher Objekt
$objSearcher.SearchRoot = New-Object System.DirectoryServices.DirectoryEntry($objsearcher) 

$objSearcher.Filter = "(&(objectCategory=Computer))"
 
#Aktiviert die rekursive Suche in Sub-OUs
$objSearcher.SearchScope = "Subtree"
#Setzt die maximale Anzahl an zurückgegebenen Nutzern auf 10000 (Standard ist 2000)
$objSearcher.PageSize = 10000;
$objects = $objSearcher.FindAll()

foreach ($obj in $objects){
    $obj.Properties | Format-Table
}
